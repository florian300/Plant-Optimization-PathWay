FRAMES = [FRAME_1, FRAME_2, FRAME_3, FRAME_4]

def scale_ascii(ascii_art, target_width):
    """
    Scales the ASCII art horizontally by dropping columns to fit the target_width.
    """
    lines = [l for l in ascii_art.split('\n') if l or len(l) > 0]
    if not lines:
        return ""
    
    original_width = max(len(l) for l in lines)
    if original_width <= target_width:
        return ascii_art
    
    # Calculate step for skipping columns
    scaling_factor = target_width / original_width
    scaled_lines = []
    
    for line in lines:
        new_line = ""
        current_acc = 0.0
        for char in line:
            current_acc += scaling_factor
            if current_acc >= 1.0:
                new_line += char
                current_acc -= 1.0
        scaled_lines.append(new_line)
        
    return "\n".join(scaled_lines)

def play_animation():
    console = Console()
    
    # Get terminal size
    size = shutil.get_terminal_size((130, 40))
    target_width = min(130, size.columns - 4) # leave some margin
    
    with Live(Align.center(Text("")), console=console, screen=True, refresh_per_second=4) as live:
        for frame in FRAMES:
            scaled_frame = scale_ascii(frame, target_width)
            live.update(Align.center(Text(scaled_frame, style="bold cyan")))
            time.sleep(0.8)
            
        # Final Loading message
        last_frame = scale_ascii(FRAMES[-1], target_width)
        loading_text = "\n[bold yellow]   INITIALIZING PATHFINDER ENGINE...[/bold yellow]"
        
        # Combine Text and Markup safely
        final_display = Text(last_frame, style="bold cyan")
        final_display.append(Text.from_markup(loading_text))
        
        live.update(Align.center(final_display))
        time.sleep(1.5)

